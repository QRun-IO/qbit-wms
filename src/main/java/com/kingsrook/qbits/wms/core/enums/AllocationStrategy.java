/*******************************************************************************
 ** Enum of inventory allocation strategies.
 *******************************************************************************/
package com.kingsrook.qbits.wms.core.enums;


import java.util.Objects;
import com.kingsrook.qqq.backend.core.model.metadata.possiblevalues.PossibleValueEnum;
import com.kingsrook.qqq.backend.core.model.metadata.producers.annotations.QMetaDataProducingPossibleValueEnum;


@QMetaDataProducingPossibleValueEnum()
public enum AllocationStrategy implements PossibleValueEnum<Integer>
{
   FEFO(1, "FEFO"),
   FIFO(2, "FIFO"),
   LIFO(3, "LIFO");

   private final Integer id;
   private final String  label;

   public static final String NAME = "AllocationStrategy";



   /*******************************************************************************
    **
    *******************************************************************************/
   AllocationStrategy(Integer id, String label)
   {
      this.id = id;
      this.label = label;
   }



   /*******************************************************************************
    ** Get instance by id
    *******************************************************************************/
   public static AllocationStrategy getById(Integer id)
   {
      if(id == null)
      {
         return (null);
      }

      for(AllocationStrategy value : AllocationStrategy.values())
      {
         if(Objects.equals(value.id, id))
         {
            return (value);
         }
      }

      return (null);
   }



   /*******************************************************************************
    ** Getter for id
    *******************************************************************************/
   public Integer getId()
   {
      return id;
   }



   /*******************************************************************************
    ** Getter for label
    *******************************************************************************/
   public String getLabel()
   {
      return label;
   }



   /*******************************************************************************
    **
    *******************************************************************************/
   @Override
   public Integer getPossibleValueId()
   {
      return (getId());
   }



   /*******************************************************************************
    **
    *******************************************************************************/
   @Override
   public String getPossibleValueLabel()
   {
      return (getLabel());
   }
}
