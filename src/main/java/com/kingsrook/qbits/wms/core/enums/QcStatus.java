/*******************************************************************************
 ** Enum of quality control statuses.
 *******************************************************************************/
package com.kingsrook.qbits.wms.core.enums;


import java.util.Objects;
import com.kingsrook.qqq.backend.core.model.metadata.possiblevalues.PossibleValueEnum;
import com.kingsrook.qqq.backend.core.model.metadata.producers.annotations.QMetaDataProducingPossibleValueEnum;


@QMetaDataProducingPossibleValueEnum()
public enum QcStatus implements PossibleValueEnum<Integer>
{
   PENDING(1, "Pending"),
   PASSED(2, "Passed"),
   FAILED(3, "Failed"),
   NOT_REQUIRED(4, "Not Required");

   private final Integer id;
   private final String  label;

   public static final String NAME = "QcStatus";



   /*******************************************************************************
    **
    *******************************************************************************/
   QcStatus(Integer id, String label)
   {
      this.id = id;
      this.label = label;
   }



   /*******************************************************************************
    ** Get instance by id
    *******************************************************************************/
   public static QcStatus getById(Integer id)
   {
      if(id == null)
      {
         return (null);
      }

      for(QcStatus value : QcStatus.values())
      {
         if(Objects.equals(value.id, id))
         {
            return (value);
         }
      }

      return (null);
   }



   /*******************************************************************************
    ** Getter for id
    *******************************************************************************/
   public Integer getId()
   {
      return id;
   }



   /*******************************************************************************
    ** Getter for label
    *******************************************************************************/
   public String getLabel()
   {
      return label;
   }



   /*******************************************************************************
    **
    *******************************************************************************/
   @Override
   public Integer getPossibleValueId()
   {
      return (getId());
   }



   /*******************************************************************************
    **
    *******************************************************************************/
   @Override
   public String getPossibleValueLabel()
   {
      return (getLabel());
   }
}
