/*******************************************************************************
 ** Enum of item condition codes.
 *******************************************************************************/
package com.kingsrook.qbits.wms.core.enums;


import java.util.Objects;
import com.kingsrook.qqq.backend.core.model.metadata.possiblevalues.PossibleValueEnum;
import com.kingsrook.qqq.backend.core.model.metadata.producers.annotations.QMetaDataProducingPossibleValueEnum;


@QMetaDataProducingPossibleValueEnum()
public enum ConditionCode implements PossibleValueEnum<Integer>
{
   NEW(1, "New"),
   DAMAGED(2, "Damaged"),
   DEFECTIVE(3, "Defective");

   private final Integer id;
   private final String  label;

   public static final String NAME = "ConditionCode";



   /*******************************************************************************
    **
    *******************************************************************************/
   ConditionCode(Integer id, String label)
   {
      this.id = id;
      this.label = label;
   }



   /*******************************************************************************
    ** Get instance by id
    *******************************************************************************/
   public static ConditionCode getById(Integer id)
   {
      if(id == null)
      {
         return (null);
      }

      for(ConditionCode value : ConditionCode.values())
      {
         if(Objects.equals(value.id, id))
         {
            return (value);
         }
      }

      return (null);
   }



   /*******************************************************************************
    ** Getter for id
    *******************************************************************************/
   public Integer getId()
   {
      return id;
   }



   /*******************************************************************************
    ** Getter for label
    *******************************************************************************/
   public String getLabel()
   {
      return label;
   }



   /*******************************************************************************
    **
    *******************************************************************************/
   @Override
   public Integer getPossibleValueId()
   {
      return (getId());
   }



   /*******************************************************************************
    **
    *******************************************************************************/
   @Override
   public String getPossibleValueLabel()
   {
      return (getLabel());
   }
}
